a = int(input())
b = int(input())
print("a+b=", a + b,sep="")#加法
print("a-b=", a - b,sep="")
print("a*b=", a * b,sep="")#
print("a/b=", a / b,sep="")#单斜杠带表除法
print("a//b=", a // b,sep="")#双斜杠只保留整数
print("a%b=", a % b,sep="")#求余
print("a**b=", a ** b,sep="")#乘方
print("a^b=", a ^ b,sep="")#会将两个数字转换成二进制，然后对每一位进行异或运算，得到的结果再转换成十进制